function getDayCodeRequirements(teamName, dayCode) {
  if (!DAY_CODE_REQUIREMENTS[teamName]) {
    console.error("Team " + teamName + " not found");
    return null;
  }
  if (!DAY_CODE_REQUIREMENTS[teamName][dayCode]) {
    console.error("Day code " + dayCode + " not found for team " + teamName);
    return null;
  }
  return DAY_CODE_REQUIREMENTS[teamName][dayCode];
}

function getDayCodeOptions(teamName) {
  if (!DAY_CODE_REQUIREMENTS[teamName]) {
    return [];
  }
  return Object.keys(DAY_CODE_REQUIREMENTS[teamName]).map(function(letter) {
    var dc = DAY_CODE_REQUIREMENTS[teamName][letter];
    return {
      letter: letter,
      name: dc.name,
      description: dc.description,
      label: letter + " - " + dc.name
    };
  });
}

function getAllDayCodeOptions() {
  var options = [];
  var seen = {};
  if (DAY_CODE_REQUIREMENTS.Phantom) {
    Object.keys(DAY_CODE_REQUIREMENTS.Phantom).forEach(function(letter) {
      if (!seen[letter]) {
        var dc = DAY_CODE_REQUIREMENTS.Phantom[letter];
        options.push({
          letter: letter,
          name: dc.name,
          description: dc.description,
          label: letter + " - " + dc.name
        });
        seen[letter] = true;
      }
    });
  }
  return options.sort(function(a, b) {
    return a.letter.localeCompare(b.letter);
  });
}

